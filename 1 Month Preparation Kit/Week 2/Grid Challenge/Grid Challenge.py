def gridChallenge(grid):
    # Write your code here
    sort_grid = [sorted(row) for row in grid]

    columns = [list(col) for col in zip(*sort_grid)]

    if all(col == sorted(col) for col in columns):
        return "YES"
    else:
        return "NO"


